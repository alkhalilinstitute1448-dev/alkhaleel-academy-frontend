/**
 * Normalize a raw phone number to E.164 format.
 * For Syrian numbers (+963), accepts:
 *   9XXXXXXXX, 09XXXXXXXX, 9639XXXXXXXX, +9639XXXXXXXX
 * and normalizes to +9639XXXXXXXX
 *
 * For non-Syrian numbers, strips non-digits.
 * Returns null for empty/invalid input.
 */
export function normalizePhone(raw) {
  if (!raw || typeof raw !== 'string') return null;

  const s = raw.replace(/[\s\-\(\)]/g, '');

  if (/^\+9639\d{8}$/.test(s)) return s;

  if (/^\+\d{7,15}$/.test(s) && !s.startsWith('+963')) return s;

  const digits = s.replace(/\D/g, '');
  if (!digits) return null;

  if (/^9639\d{8}$/.test(digits)) return `+963${digits}`;
  if (/^09\d{8}$/.test(digits)) return `+9639${digits.slice(2)}`;
  if (/^9\d{8}$/.test(digits)) return `+963${digits}`;

  if (digits.length === 8) return `+9639${digits}`;

  if (digits.startsWith('963') || digits.startsWith('+963')) return null;

  return digits;
}

/**
 * Build the full E.164 phone number from country code + digits.
 * For Syria: normalizes raw input to +9639XXXXXXXX via normalizePhone.
 * For other countries: adds dial code + afterDial to the local digits.
 */
export function buildFullPhone(countryCode, digits) {
  if (countryCode === 'SY') return normalizePhone(digits) || digits;
  const c = COUNTRIES.find((c2) => c2.code === countryCode) || COUNTRIES[0];
  return c.dial + (c.afterDial || '') + digits;
}

/**
 * Extract the local display portion from a Syrian E.164 number.
 * +9639XXXXXXXX → XXXXXXXX (8 local digits)
 */
export function extractLocalDigits(e164) {
  if (!e164) return '';
  if (e164.startsWith('+9639')) return e164.slice(5);
  return e164;
}
